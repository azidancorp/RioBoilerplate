def _load_common_passwords() -> frozenset[str]:
    compressed = base64.b64decode(_COMPRESSED_B64, validate=True)
    if hashlib.sha256(compressed).hexdigest() != SOURCE_GZIP_SHA256:
        raise RuntimeError("Embedded common-password gzip hash does not match its source")

    contents = gzip.decompress(compressed)
    if hashlib.sha256(contents).hexdigest() != SOURCE_TEXT_SHA256:
        raise RuntimeError("Embedded common-password text hash does not match its source")

    passwords = frozenset(
        line.strip().casefold()
        for line in contents.decode("utf-8").splitlines()
        if line.strip()
    )
    if len(passwords) != ENTRY_COUNT:
        raise RuntimeError(
            "Embedded common-password entry count does not match its source"
        )
    return passwords


COMMON_PASSWORDS = _load_common_passwords()
